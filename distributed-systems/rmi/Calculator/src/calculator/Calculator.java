package calculator;


public class Calculator{

	public double plus(double x, double y)
        {
            return x+y;
        }
        public double minus(double x, double y)
        {
            return x-y;
        }  
        public double times(double x, double y)
        {
            return x*y;
        }
        public double division(double x, double y)
        {
            return x/y;
        }
}
