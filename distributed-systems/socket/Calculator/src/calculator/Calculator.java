package calculator;

public class Calculator {
    
    public double plus(double a, double b){
        return a+b;
    }
    
    public double minus(double a, double b){
        return a-b;
    }
    
    public double times(double a, double b){
        return a*b;
    }

    public double division(double a, double b){
        return a/b;
    }
}
